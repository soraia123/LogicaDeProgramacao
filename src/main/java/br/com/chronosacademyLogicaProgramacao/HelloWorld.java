package br.com.chronosacademyLogicaProgramacao;

public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("HelloWorld");

    //  tipo    variavel/objeto resultado
        boolean encontrado = false;
        System.out.println(encontrado);

    // char é aspas simples e só aceita um caracter
    // string é aspas dupla
        char varChar = 'A';
        System.out.println(varChar);

    //float  tem que colocar a letra f no final
    //double não precisa colocar letra que é aceito

    float varFloat = 12.5f;
    double varDouble = 12.5;
    System.out.println(varDouble);

    //byte vai de −128 a 127
    byte varByte = 127;
    System.out.println(varByte);

    //short vai de -32768 a 32767
    short varShort = 32767;
    System.out.println(varShort);

    //int vai de -2147483648 a 2147483647
    int varInt = 2147483647;
    System.out.println(varInt);

    //long vai de -9223372036854775808 a 9223372036854775807 e deve colocar a letra l no final
    long varLong = 9223372036854775807l;
    System.out.println(varLong);

    String nome = "Leila";
    Integer idade = 47;
    System.out.println(nome);
    System.out.println(idade);









    }
}
